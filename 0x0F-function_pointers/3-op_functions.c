#include "3-calc.h"

/**
 * op_add - function that add two numbers
 * @a: nummber 1
 * @b: number 2
 * Return: result
 * On error, return NULL
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that substract two numbers
 * @a: nummber 1
 * @b: number 2
 * Return: result
 * On error, return NULL
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiply two numbers
 * @a: nummber 1
 * @b: number 2
 * Return: result
 * On error, return NULL
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divide two numbers
 * @a: nummber 1
 * @b: number 2
 * Return: result
 * On error, return NULL
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that obtain mod two numbers
 * @a: nummber 1
 * @b: number 2
 * Return: result
 * On error, return NULL
 */
int op_mod(int a, int b)
{
	return (a % b);
}
